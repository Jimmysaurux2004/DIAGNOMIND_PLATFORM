    <div class="trastorno">
      <h3 class="nombre-trastorno">1. Trastorno por Déficit de Atención e Hiperactividad (TDAH en adultos)</h3>
      <p>Es un trastorno neuropsiquiátrico caracterizado por patrones persistentes de inatención, hiperactividad e impulsividad, que afectan el funcionamiento social, académico y laboral en la vida adulta.</p>
      <ul class="lista-sintomas">
        <li>● Falta de concentración (Frecuente): Dificultad sostenida para enfocar la atención.</li>
        <li>● Impulsividad (Clave): Actuar sin pensar en consecuencias.</li>
        <li>● Hiperactividad o inquietud física (Clave): Necesidad constante de moverse o actividad motora excesiva.</li>
        <li>● Dificultad para organizar tareas cotidianas (Frecuente): Problemas para estructurar actividades diarias.</li>
        <li>● Procrastinación frecuente (Frecuente): Retrasar tareas de forma repetitiva.</li>
        <li>● Dificultad para mantenerse sentado por mucho tiempo (Posible): Inquietud persistente en contextos sedentarios.</li>
        <li>● Irritabilidad persistente (Posible): Cambios frecuentes de humor o baja tolerancia a la frustración.</li>
        <li>● Dificultad para relajarse incluso en momentos tranquilos (Posible): Ansiedad basal constante.</li>
      </ul>
    </div>

    <div class="trastorno">
      <h3 class="nombre-trastorno">2. Ansiedad Generalizada</h3>
      <p>Se manifiesta por una preocupación persistente y excesiva sobre diversas áreas de la vida cotidiana, acompañada de síntomas físicos de tensión.</p>
      <ul class="lista-sintomas">
        <li>● Falta de concentración (Frecuente)</li>
        <li>● Preocupación excesiva difícil de controlar (Clave)</li>
        <li>● Problemas crónicos para dormir (Frecuente)</li>
        <li>● Fatiga constante sin causa física (Frecuente)</li>
        <li>● Irritabilidad persistente (Frecuente)</li>
        <li>● Miedo a situaciones sociales o ser juzgado por otros (Posible)</li>
        <li>● Dificultad para relajarse incluso en momentos tranquilos (Posible)</li>
      </ul>
    </div>

    <div class="trastorno">
      <h3 class="nombre-trastorno">3. Depresión Mayor</h3>
      <p>Trastorno del estado de ánimo que se caracteriza por tristeza persistente, pérdida de interés en actividades y alteraciones en funciones biológicas.</p>
      <ul class="lista-sintomas">
        <li>● Falta de concentración (Frecuente)</li>
        <li>● Tristeza profunda o prolongada (Clave)</li>
        <li>● Problemas crónicos para dormir (Frecuente)</li>
        <li>● Procrastinación frecuente (Posible)</li>
        <li>● Fatiga constante sin causa física (Frecuente)</li>
        <li>● Pérdida de interés o placer en casi todas las actividades (Clave)</li>
        <li>● Sensación de fracaso o culpa excesiva (Posible)</li>
      </ul>
    </div>

    <div class="trastorno">
      <h3 class="nombre-trastorno">4. Trastorno Bipolar Tipo II</h3>
      <p>Se caracteriza por cambios de humor que incluyen episodios de depresión mayor y periodos de hipomanía (energía alta no severa).</p>
      <ul class="lista-sintomas">
        <li>● Falta de concentración (Frecuente)</li>
        <li>● Impulsividad (Posible)</li>
        <li>● Tristeza profunda o prolongada (Frecuente)</li>
        <li>● Ciclos de energía alta y baja (Clave)</li>
        <li>● Fatiga constante sin causa física (Frecuente)</li>
        <li>● Irritabilidad persistente (Frecuente)</li>
        <li>● Pensamientos acelerados en ciclos (Posible)</li>
        <li>● Necesidad de dormir muy poco con mucha energía (Clave)</li>
      </ul>
    </div>

